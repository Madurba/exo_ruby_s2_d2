# texte + variable muliplicatrice de nombres entiers, calcul la somme
puts "Travail : #{10 * 5 * 11}"
# Affiche le texte entre guillemet
puts "Et en secondes ?"
# formule multiplicatrice de nombres entiers
puts 10 * 5 * 11 * 60 * 60
# Affiche le texte entre guillemet
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7"
#formule arithmetique true or false 
puts 3 + 2 < 5 - 7
# Affiche le texte entre guillemet + opérateur de variable
puts "Ça fait combien 3 + 2 ? #{3 + 2}"
